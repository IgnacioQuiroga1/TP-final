    private void btnOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOkActionPerformed

        SqlUs = new SqlUsuarios();
        us = new Usuario();
        String contraseña1 = new String(vLogin.txtContraseña.getPassword());
        String nombre = us.getNombre();

        us.setCorreo(this.txtCorreo.getText());
        us.setContraseña(contraseña1);
       
        
        if (SqlUs.login(us)) {
            
            vLogin.dispose();
 
            apps = new VentanaApps();
            apps.setVisible(true);

            JOptionPane.showMessageDialog(null, "OK!!!");

            limpiar();
        } else {
            JOptionPane.showMessageDialog(null, "Datos incorrectos");
            limpiar();
        }
        
        app = new VentanaApps();
      
    }//GEN-LAST:event_btnOkActionPerformed

